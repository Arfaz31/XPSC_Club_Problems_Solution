#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int T;
    cin >> T;
    while (T--)
    {
        int N;
        cin >> N;

        // reverse permutation gives minimal LCM
        for (int i = 1; i <= N; i++)
        {
            cout << (N - i + 1) << " ";
        }
        cout << "\n";
    }
    return 0;
}

